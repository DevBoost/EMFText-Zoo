package org.emftext.language.bibtex.resource.bib.mopp;

/**
 * An Excpetion to represent invalid content types for parser instances.
 * 
 * @see org.emftext.language.bibtex.resource.bib.IBibOptions.RESOURCE_CONTENT_TYPE
 */
public class BibUnexpectedContentTypeException extends org.antlr.runtime3_2_0.RecognitionException{
	
	private static final long serialVersionUID = 4791359811519433999L;
	
	private Object contentType = null;
	
	public  BibUnexpectedContentTypeException(Object contentType) {
		this.contentType = contentType;
	}
	
	public Object getContentType() {
		return contentType;
	}
	
}
