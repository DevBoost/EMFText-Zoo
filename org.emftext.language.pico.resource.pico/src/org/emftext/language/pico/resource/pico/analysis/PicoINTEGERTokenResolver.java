package org.emftext.language.pico.resource.pico.analysis;

public class PicoINTEGERTokenResolver implements org.emftext.language.pico.resource.pico.IPicoTokenResolver {
	
	private org.emftext.language.pico.resource.pico.analysis.PicoDefaultTokenResolver defaultTokenResolver = new org.emftext.language.pico.resource.pico.analysis.PicoDefaultTokenResolver(true);
	
	public String deResolve(Object value, org.eclipse.emf.ecore.EStructuralFeature feature, org.eclipse.emf.ecore.EObject container) {
		// By default token de-resolving is delegated to the DefaultTokenResolver.
		String result = defaultTokenResolver.deResolve(value, feature, container, null, null, null);
		return result;
	}
	
	public void resolve(String lexem, org.eclipse.emf.ecore.EStructuralFeature feature, org.emftext.language.pico.resource.pico.IPicoTokenResolveResult result) {
		// By default token resolving is delegated to the DefaultTokenResolver.
		defaultTokenResolver.resolve(lexem, feature, result, null, null, null);
	}
	
	public void setOptions(java.util.Map<?,?> options) {
		defaultTokenResolver.setOptions(options);
	}
	
}
