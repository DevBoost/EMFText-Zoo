package org.emftext.language.bibtex.resource.bib.ui;

public class BibQuickAssistAssistant extends org.eclipse.jface.text.quickassist.QuickAssistAssistant implements org.eclipse.jface.text.quickassist.IQuickAssistAssistant {
	
	public BibQuickAssistAssistant(org.emftext.language.bibtex.resource.bib.IBibResourceProvider resourceProvider, org.emftext.language.bibtex.resource.bib.ui.IBibAnnotationModelProvider annotationModelProvider) {
		setQuickAssistProcessor(new org.emftext.language.bibtex.resource.bib.ui.BibQuickAssistProcessor(resourceProvider, annotationModelProvider));
		setInformationControlCreator(new org.eclipse.jface.text.AbstractReusableInformationControlCreator() {
			public org.eclipse.jface.text.IInformationControl doCreateInformationControl(org.eclipse.swt.widgets.Shell parent) {
				return new org.eclipse.jface.text.DefaultInformationControl(parent, (org.eclipse.jface.text.DefaultInformationControl.IInformationPresenter) null);
			}
		});
	}
	
	public boolean canAssist(org.eclipse.jface.text.quickassist.IQuickAssistInvocationContext invocationContext) {
		return false;
	}
	
	public boolean canFix(org.eclipse.jface.text.source.Annotation annotation) {
		return true;
	}
	
}
