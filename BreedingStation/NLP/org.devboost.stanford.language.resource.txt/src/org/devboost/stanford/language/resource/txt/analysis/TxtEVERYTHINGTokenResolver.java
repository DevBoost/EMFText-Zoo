package org.devboost.stanford.language.resource.txt.analysis;

public class TxtEVERYTHINGTokenResolver implements org.devboost.stanford.language.resource.txt.ITxtTokenResolver {
	
	private org.devboost.stanford.language.resource.txt.analysis.TxtDefaultTokenResolver defaultTokenResolver = new org.devboost.stanford.language.resource.txt.analysis.TxtDefaultTokenResolver(true);
	
	public String deResolve(Object value, org.eclipse.emf.ecore.EStructuralFeature feature, org.eclipse.emf.ecore.EObject container) {
		// By default token de-resolving is delegated to the DefaultTokenResolver.
		String result = defaultTokenResolver.deResolve(value, feature, container, null, null, null);
		return result;
	}
	
	public void resolve(String lexem, org.eclipse.emf.ecore.EStructuralFeature feature, org.devboost.stanford.language.resource.txt.ITxtTokenResolveResult result) {
		// By default token resolving is delegated to the DefaultTokenResolver.
		defaultTokenResolver.resolve(lexem, feature, result, null, null, null);
	}
	
	public void setOptions(java.util.Map<?,?> options) {
		defaultTokenResolver.setOptions(options);
	}
	
}
